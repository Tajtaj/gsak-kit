package org.projectspinoza.gsakkit.layouts;

public class LayoutSettings {

	YifanHuLayoutSetting yifanHuLayout;
	ForceAtlasLayoutSetting forceAtlasLayout;
	FruchtermanReingoldSetting fruchtermanReingold;
	String nodeSizeBy;

	public YifanHuLayoutSetting getYifanHuLayout() {
		return yifanHuLayout;
	}

	public void setYifanHuLayout(YifanHuLayoutSetting yifanHuLayout) {
		this.yifanHuLayout = yifanHuLayout;
	}

	public ForceAtlasLayoutSetting getForceAtlasLayout() {
		return forceAtlasLayout;
	}

	public void setForceAtlasLayout(ForceAtlasLayoutSetting forceAtlasLayout) {
		this.forceAtlasLayout = forceAtlasLayout;
	}

	public FruchtermanReingoldSetting getFruchtermanReingold() {
		return fruchtermanReingold;
	}

	public void setFruchtermanReingold(FruchtermanReingoldSetting fruchtermanReingold) {
		this.fruchtermanReingold = fruchtermanReingold;
	}

	public String getNodeSizeBy() {
		return nodeSizeBy;
	}
	public void setNodeSizeBy(String nodeSizeBy) {
		this.nodeSizeBy = nodeSizeBy;
	}
	

	
}
